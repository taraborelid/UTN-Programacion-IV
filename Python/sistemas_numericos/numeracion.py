# Profundizando en los sistemas de numeracion

# Sistema Decimal
a = 10
print(f"a decimal: {a}")

# Sistema binario
a = 0b1011
print(f"a binario: {a}")

# Sistema octal
a = 0o12
print(f"a octal: {a}")

# Sistema hexadecimal
a = 0xA
print(f"a hexadecimal: {a}")

a = int('23', 10)
print(f"a decimal desde string: {a}")

a = int('1011', 2)
print(f"a binario desde string: {a}")

a = int('27', 8)
print(f"a octal desde string: {a}")

a = int('A', 16)
print(f"a hexadecimal desde string: {a}")

# Base 5 sus valores son de 0 a 4
a = int('34', 5)
print(f"a base 5: {a}")